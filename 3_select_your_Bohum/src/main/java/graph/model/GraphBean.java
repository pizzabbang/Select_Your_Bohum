package graph.model;

public class GraphBean {
	private String x;
	private int y;
	public GraphBean() {
		super();
	}
	public GraphBean(String x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
}
